/** \brief retorna el mayor de 3 enteros
 *
 * \param a int  primer entero a evaluar
 * \param b int  segundo entero a evaluar
 * \param c int  tercer entero a evaluar
 * \return int  el mayor de los parametros ingresados
 *
 */
